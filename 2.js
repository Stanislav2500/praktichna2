// Запитуємо вік користувача через prompt
let age = prompt("Введіть ваш вік:");

// Перевіряємо вік користувача і виводимо відповідне повідомлення в консоль
if (age < 18) {
  console.log("Вам заборонено вхід");
} else if (age >= 18 && age <= 65) {
  console.log("Ласкаво просимо!");
} else if (age > 65) {
  console.log("Будь ласка, будьте обережні!");
}
